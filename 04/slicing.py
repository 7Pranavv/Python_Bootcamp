name="Pranav123456789" 
print(name[0:3]) # prints 'Pra' (from index 0 to 2)


print(name[2:-1]) # prints 'ana' (from index 2 to the second last character) #same as name[2:5]


print(name[0:10:1])# skip 0 char
print(name[0:10:2]) # skip 1 char 
print(name[0:10:3]) # skip 2 char

print(name[:4]) # prints 'Pran' (from start to index 3)
print(name[1:]) # prints 'ranav123456789' (from index 1 to the end)